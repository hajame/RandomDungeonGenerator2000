
package dungeongenerator.util;

/**
 *Class that holds values for x and y positions. 
 *Intended to be modified from the outside.
 * 
 * @author hajame
 */
public class Position {
    public int x;
    public int y;

    public Position(int x, int y) {
        this.x = x;
        this.y = y;
    }
    
    
}
